// 成人、兒童口罩篩選
function btnShowMask() {
    console.log("btnShowMask");
    let maskBtn = my$('fliterMask').getElementsByTagName('input');
    for (let i = 0; i < maskBtn.length; i++) {
        maskBtn[i].onclick = function () {
            switch (maskBtn[i].id) {
                case 'btn__adultMask':
                    // showMask = "adult";
                    updateStoreList('adult');
                    break;
                case 'btn__childMask':
                    // showMask = "child";
                    updateStoreList('child');
                    break;
                default:
                    // showMask = "all";
                    updateStoreList('all');
            }
        }
    }
}
